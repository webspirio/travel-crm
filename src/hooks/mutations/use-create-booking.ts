import { useMutation, useQueryClient } from "@tanstack/react-query"

import { supabase } from "@/lib/supabase"
import { requireTenant, resolveManagerId } from "@/lib/auth-guard"
import { bookingsKeys } from "@/hooks/queries/use-bookings"
import { clientsKeys } from "@/hooks/queries/use-clients"
import { tripsKeys, tripOccupancyKeys } from "@/hooks/queries/use-trips"
import { tripSeatsKeys } from "@/hooks/queries/use-trip-seats"
import type { LegacyBookingDraft } from "@/stores/booking-store"
import type { Client } from "@/types"
import type { Database } from "@/types/database"

type BookingRow = Database["public"]["Tables"]["bookings"]["Row"]

export interface CreateBookingInput {
  /** Snapshot of the booking-store state after the last wizard step. */
  draft: LegacyBookingDraft
}

export interface CreateBookingResult {
  booking: BookingRow
  /** True when a new client row was created as part of this mutation. */
  clientCreated: boolean
}

/**
 * Persists a new booking to Supabase in three sequential writes:
 *
 * 1. (Conditional) INSERT into `clients` when draft.clientId is null and
 *    draft.newClient is present.
 * 2. INSERT into `bookings` — booking_number is auto-generated by the
 *    `bookings_set_booking_number` BEFORE INSERT trigger; we pass an empty
 *    string which the trigger recognises as "generate me one".
 * 3. INSERT into `booking_passengers` with the single adult passenger from
 *    the wizard.
 *
 * On success: invalidates bookings, trips (detail only), trip-seats,
 * trip-occupancy, and (when a new client was created) clients query caches.
 *
 * Auth is read inside mutationFn via useAuthStore.getState() so call sites
 * don't need to supply tenantId / managerId. Throws "no_session" or
 * "no_manager" (dev-facing, not expected in normal flow) when the JWT or
 * managers row is absent.
 */
export function useCreateBooking() {
  const queryClient = useQueryClient()

  return useMutation<CreateBookingResult, Error, CreateBookingInput>({
    mutationFn: async ({ draft }) => {
      // ── Step 0: resolve auth identities ───────────────────────────────
      const { tenantId, userId } = requireTenant()
      const managerId = await resolveManagerId(queryClient, userId)

      // ── Step 1: resolve client id ──────────────────────────────────────
      let clientId = draft.clientId

      if (!clientId) {
        if (!draft.newClient) {
          throw new Error("No client selected and no new-client data provided.")
        }
        const { data: clientData, error: clientErr } = await supabase
          .from("clients")
          .insert({
            tenant_id: tenantId,
            first_name: draft.newClient.firstName,
            last_name: draft.newClient.lastName,
            email: draft.newClient.email,
            phone: draft.newClient.phone,
            nationality: draft.newClient.nationality,
          })
          .select("id")
          .single()

        if (clientErr) throw clientErr
        clientId = clientData.id
      }

      // ── Step 2: guard required fields ─────────────────────────────────
      if (!draft.tripId) throw new Error("No trip selected.")
      if (draft.seatNumber === null) throw new Error("No seat selected.")

      const pricing = draft.pricing ?? { basePrice: 0, hotelCost: 0, total: 0, commission: 0 }

      // ── Step 3: INSERT booking ─────────────────────────────────────────
      // booking_number is `text not null` in the schema but the BEFORE INSERT
      // trigger (bookings_set_booking_number) replaces it when it is empty.
      const { data: bookingData, error: bookingErr } = await supabase
        .from("bookings")
        .insert({
          tenant_id: tenantId,
          client_id: clientId,
          trip_id: draft.tripId,
          sold_by_manager_id: managerId,
          booking_number: "",
          total_price_eur: pricing.total,
          commission_eur: pricing.commission,
          // paid_amount_eur defaults to 0 in the DB — no need to pass it
          // status defaults to 'draft' via the DB column default
        })
        .select()
        .single()

      if (bookingErr) throw bookingErr

      // ── Step 4: resolve passenger name ────────────────────────────────
      // New-client path: name is already in draft.newClient (captured before
      // clientId was assigned above). Existing-client path: cache-first
      // lookup to avoid a redundant round-trip on the common path.
      let passengerFirstName: string
      let passengerLastName: string

      if (draft.newClient && !draft.clientId) {
        // We just created this client — names are in the draft.
        passengerFirstName = draft.newClient.firstName
        passengerLastName = draft.newClient.lastName
      } else {
        // Existing client — try the react-query cache first.
        const cachedClients = queryClient.getQueryData<Client[]>(clientsKeys.lists())
        const cachedClient = cachedClients?.find((c) => c.id === clientId)
        if (cachedClient) {
          passengerFirstName = cachedClient.firstName
          passengerLastName = cachedClient.lastName
        } else {
          // Cache miss (e.g. direct navigation to /bookings/new) — fall back
          // to a fresh SELECT.
          const { data: clientRow, error: lookupErr } = await supabase
            .from("clients")
            .select("first_name, last_name")
            .eq("id", clientId)
            .single()
          if (lookupErr) throw lookupErr
          passengerFirstName = clientRow.first_name
          passengerLastName = clientRow.last_name
        }
      }

      // ── Step 5: INSERT passenger ───────────────────────────────────────
      const { error: passengerErr } = await supabase.from("booking_passengers").insert({
        tenant_id: tenantId,
        booking_id: bookingData.id,
        trip_id: draft.tripId,
        kind: "adult",
        first_name: passengerFirstName,
        last_name: passengerLastName,
        seat_number: draft.seatNumber,
        hotel_id: draft.hotelId ?? null,
        room_type: draft.roomType ?? null,
        price_total_eur: pricing.total,
        price_breakdown: {
          basePrice: pricing.basePrice,
          hotelCost: pricing.hotelCost,
          total: pricing.total,
          commission: pricing.commission,
        },
      })

      if (passengerErr) throw passengerErr

      return {
        booking: bookingData,
        clientCreated: !draft.clientId,
      }
    },

    onSuccess: ({ clientCreated }, { draft }) => {
      // Invalidate all booking-related caches so lists and detail pages
      // reflect the new booking immediately.
      void queryClient.invalidateQueries({ queryKey: bookingsKeys.all })

      // Only the trip's own detail page changes (seat count etc.); the
      // trips list index doesn't carry derived booking counts.
      if (draft.tripId) {
        void queryClient.invalidateQueries({
          queryKey: tripsKeys.detail(draft.tripId),
        })
        void queryClient.invalidateQueries({
          queryKey: tripSeatsKeys.byTrip(draft.tripId),
        })
        void queryClient.invalidateQueries({
          queryKey: tripOccupancyKeys.byTrip(draft.tripId),
        })
      }

      // Invalidate clients only when we created a new one; the existing
      // client list doesn't change otherwise.
      if (clientCreated) {
        void queryClient.invalidateQueries({ queryKey: clientsKeys.all })
      }
    },
  })
}
